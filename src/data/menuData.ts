// Helper function to get menu items for a specific canteen
export const getCanteenMenuItems = (canteenId: number): UnifiedMenuItem[] => {
  return CANTEEN_MENU_DATA[canteenId as keyof typeof CANTEEN_MENU_DATA] || [];
};

// Helper function to initialize localStorage with correct data
export const initializeCanteenData = (canteenId: number) => {
  const menuItems = getCanteenMenuItems(canteenId);
  const adminStorageKey = `canteen_${canteenId}_admin_menu`;
  const studentStorageKey = `canteen_${canteenId}_menu`;

  // Set both admin and student data to the same items
  localStorage.setItem(adminStorageKey, JSON.stringify(menuItems));
  localStorage.setItem(studentStorageKey, JSON.stringify(menuItems));

  return menuItems;
};

// Helper function to get category counts for verification
export const getCanteenMenuStats = (canteenId: number) => {
  const items = getCanteenMenuItems(canteenId);
  const vegCount = items.filter(item => item.is_veg).length;
  const nonVegCount = items.filter(item => !item.is_veg).length;
  const categories = [...new Set(items.map(item => item.category))];
  
  return {
    total: items.length,
    veg: vegCount,
    nonVeg: nonVegCount,
    categories: categories.sort(),
    canteenName: canteenId === 1 ? 'Main Canteen' : canteenId === 2 ? 'IT Canteen' : 'MBA Canteen'
  };
};